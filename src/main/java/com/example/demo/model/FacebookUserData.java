package com.example.demo.model;

public class FacebookUserData {

	private int id;
	private int walkInId;
	private String fbUserName;
	private String fbEmailId;
	private int Interest;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getWalkInId() {
		return walkInId;
	}
	public void setWalkInId(int walkInId) {
		this.walkInId = walkInId;
	}
	public String getFbUserName() {
		return fbUserName;
	}
	public void setFbUserName(String fbUserName) {
		this.fbUserName = fbUserName;
	}
	public String getFbEmailId() {
		return fbEmailId;
	}
	public void setFbEmailId(String fbEmailId) {
		this.fbEmailId = fbEmailId;
	}
	public int getInterest() {
		return Interest;
	}
	public void setInterest(int interest) {
		Interest = interest;
	}
	
	
}
